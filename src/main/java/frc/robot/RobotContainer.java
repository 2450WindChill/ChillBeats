    // Schedule `ExampleCommand` when `exampleCondition` changes to `true`
    buttonA.onTrue(new ExampleCommand(m_drivetrainSubsystem));


    

    // Schedule `exampleMethodCommand` when the Xbox controller's B button is pressed,
